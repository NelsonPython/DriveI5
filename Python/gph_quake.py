import pandas as pd
import matplotlib.pyplot as plt

sp = pd.read_csv("sensorData/AstroPiQuake.csv")

plt.figure()
plt.title("AstroPiQuake Accelerometer Readings")
plt.xlabel("Minutes running")
plt.ylabel("")
plt.plot(sp["x"], label="x")
plt.plot(sp["y"], label="y")
plt.plot(sp["z"], label="z")
plt.legend()
plt.show()

plt.figure()
plt.title("AstroPiQuake Gyroscope Readings")
plt.xlabel("Minutes running")
plt.ylabel("")
plt.plot(sp["yaw"], label="yaw")
plt.plot(sp["pitch"], label="pitch")
plt.plot(sp["roll"], label="roll")
plt.legend()
plt.show()

ev = pd.read_csv("sensorData/Enviro.csv")

plt.figure()
plt.title("Enviro Accelerometer Readings")
plt.xlabel("Minutes running")
plt.ylabel("")
plt.plot(ev["x"], label="x")
plt.plot(ev["y"], label="y")
plt.plot(ev["z"], label="z")
plt.legend()
plt.show()


plt.figure()
plt.title("Combined Accelerometer Readings")
plt.xlabel("Minutes running")
plt.ylabel("")
plt.plot(sp["x"], label="AstroPiQuake x")
plt.plot(sp["y"], label="AstroPiQuake y")
plt.plot(sp["z"], label="AstroPiQuake z")
plt.plot(ev["x"], label="Enviro x")
plt.plot(ev["y"], label="Enviro y")
plt.plot(ev["z"], label="Enviro z")
plt.legend()
plt.show()
